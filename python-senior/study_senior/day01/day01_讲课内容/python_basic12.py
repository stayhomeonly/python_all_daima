print("侬好")

a = '今天天气很好，和你一样'  # a是一个变量，把后面这句话储存在a里面
print(a)  # 打印a的内容  今天天气很好，和你一样

# Ctrl + Alt + L  自动调整格式的快捷键
# 声明变量，变量是用来存储值的

# ******************** 知识点1: 变量的声明
a = 1  # 该行代码声明了一个变量a,并且赋值1,变量a是整数类型 int
b = 2  # 该行代码声明了一个变量b,并且赋值2,变量b是整数类型 int
print(a + b)  # 3

c = 3.5  # 该行代码声明了一个变量c,并且赋值3.5,变量c是浮点数类型 float
print(b + c)  # 5.5

# 保存字符串可以使用三种方式，一般使用前两种
name1 = 'xiaohua'  # 该行代码声明了一个变量name，并且赋值xiaohua，变量name是字符串类型  str
name2 = "xiaozi"
name3 = '''xiaomei'''
name4 = """xiaolan"""

# ******************** 知识点2: 变量的覆盖，变量可以一次声明、多次赋值，后面的值会覆盖前面的值
sal = 1000  # 声明sal变量，赋值为1000、
print(sal)  # 1000
sal = 2000  # 声明sal变量，赋值为2000
print(sal)  # 2000
sal = sal + 500  # 使用点名器回答该问题
print(sal)  # 先运行sal+500，再赋值给sal   2500

name = 'xiaohua'
print(name)  # xiaohua
name = 'xiaoliu'  # 使用点名器回答该问题
print(name)  # xiaoliu

# ******************** 知识点3: 同时为多个变量赋值
# 1.为多个变量赋值相同的值
a = b = c = 1  # 代表声明了a，b，c三个变量，且值为1
print(a, b, c)  # 1 1 1

# 2.为多个变量赋值不同的值
A, B, C = 1, 'xiaohua', 5.5  # 代表声明了A，B，C三个变量且赋予不同的值

# ******************** 知识点4: python是大小写和缩进敏感的编程语言
sno = 1001
Sno = 1002
print('sno的值是:', sno)  # sno的值是: 1001
print('Sno的值是:', Sno)  # Sno的值是: 1002

